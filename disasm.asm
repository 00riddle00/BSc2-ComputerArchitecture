; ============================================================
;  DATA
; ============================================================

.data
    data_octal db 0, 1, 6                      ; 0100: 0E       | PUSH CS
               db 0, 1, 1                      ; 0101: 09       | 011
               db 0, 3, 6                      ; 0102: 1E       | PUSH DS
               db 8, 4, 4                      ; 0XXX: ??       | UNDEFINED
               db 0, 0, 7                      ; 0XXX: ??       | POP ES
               db 2, 6, 4,  0, 1, 1            ; 0XXX: B4 09    | MOV AH, 011
               db 2, 7, 2,  3, 3, 6,  0, 0, 1  ; 0XXX: BA DE 01 | MOV DX, 001336
               db 0, 2, 7                      ; 0XXX: ??       | POP SS 
               db 0, 2, 5                      ; 0XXX: 15       | 025
               db 0, 6, 7                      ; 0XXX: 37       | 0x7_add_sub_adjust
               db 1, 4, 4                      ; 0XXX: 64       | UNDEFINED
               db 3, 2, 6                      ; 0XXX: D6       | UNDEFINED
               db 0FFh

    ; Rb = Byte-sized register
    Rb dw 'AL', 'CL', 'DL', 'BL', 'AH', 'CH', 'DL', 'BH'

    ; Rw = Word-sized register
    Rw dw 'AX', 'CX', 'DX', 'BX', 'SP', 'BP', 'SI', 'DI'

    ; SR = Segment register:
    SR dw 'ES', 'CS', 'SS', 'DS'  

    sep1 db '=============================================================================$'

; ============================================================
;  CODE
; ============================================================

.code

; ------------------------------------------------------------
; PROCEDURES
; ------------------------------------------------------------
; ...
; -----------------------------------------------------------/

start:
    mov ax, @data                  ; perkelti data i registra ax
    mov ds, ax                     ; perkelti ax (data) i data segmenta
    mov es, ax                     ; perkelti ax (data) i data segmenta

    ; isvesti programos aprasa
    m_println sep1
    m_puts   '                                   '
    m_puts   'DISASM'
    m_putsln '                                   '
    m_println sep1

    xor ax, ax
    xor si, si

    mov si, 0FFFFh

_xxx:
    ; get 1st octal digit
    inc si
    mov al, byte ptr [data_octal+si]

    cmp al, 0FFh
    je short exit_program

    cmp al, 3
    je _3xx
    ja short undefined_1st_octal

    cmp al, 1
    jb short _0xx
    je _1xx
    jmp _2xx

    jmp short _xxx
; -----------------------------------------------------------/

undefined_1st_octal:
    inc si
    inc si
    jmp short undefined

undefined_2nd_octal:
    inc si
    jmp short undefined

undefined:
    m_putsln 'UNDEFINED'
    jmp short _xxx

exit_program:
    m_exit0

; ============================================================
;  _0XX
; ============================================================

_0xx:
    ; get 2nd octal digit
    inc si
    mov al, byte ptr [data_octal+si]

    ; get 3rd octal digit
    inc si ; SI now also points to 3rd octal
    mov bl, byte ptr [data_octal+si]

    cmp al, 7
    ja undefined ; undefined_2nd_octal is not used here, since 
                 ; SI already points to the last octal digit

    ; --------- check the 3rd octal digit -------------

    ; check if it's a (POP seg)/(adjust) operation
    cmp bl, 7
    je short __0x7
    ja undefined

    ; check if it's a (PUSH seg)/(seg change prefix) operation
    cmp bl, 6
    je short __0x6

    ; --------- check the 2nd octal digit -------------
    cmp al, 4
    jb short __0_0123_x
    je _04x
    jmp short __0_567_x
    
    __0_0123_x:
        cmp al, 2
        jb short __0_01_x
        je _02x
        jmp _03x

    __0_567_x:
        cmp al, 6
        jb _05x
        je _06x
        jmp _07x

    __0_01_x:
        cmp al, 1
        jb short _00x
        jmp _01x

    ; if it's a (PUSH seg)/(seg change prefix) operation
    __0x6:
        cmp al, 4
        jb _0x6_push_seg
        jmp _0x6_seg_change_prefix

    ; if it's a (POP seg)/(adjust) operation
    __0x7:
        cmp al, 4
        jb _0x7_pop_seg
        jmp _0x7_add_sub_adjust

